const int N=2e3,oo=1e9+7,mod=1e9+7;
ll x,n,a[16][9],t,half,ans;
map<ll ,ll > mp;

ll mod_pow(ll a,ll b,ll mod){
    ll ans=1ll;
    while(b){
        if(b&1)ans=ans*a%mod;
        a=a*a%mod;
        b>>=1;
    }
    return ans;
}

void btca(int i,ll sum){
if(i==(n/2)){mp[sum]++;return ;}
for(int j=0;j<6;j++){btca(i+1,(sum*a[i][j])%mod);}
}

void btcb(int i,ll sum){
if(i==(n)){ll tmp=x*mod_pow(sum,mod-2,mod)%mod;ans+=mp[tmp];return ;}
for(int j=0;j<6;j++){btcb(i+1,(sum*a[i][j])%mod);}
}

int main(){
scanf("%lld",&t);
while(t--){
    scanf("%lld%lld",&n,&x);
    for(int i=0;i<n;i++){
        for(int j=0;j<6;j++){scanf("%lld",&a[i][j]);}
    }
    mp.clear();
    ans=0;
    btca(0,1);
    btcb(n/2,1);
   // ans%=mod;
    printf("%lld\n",ans);
 }
}
